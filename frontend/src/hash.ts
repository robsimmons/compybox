/**
 * Takes a string and produces the SHA-256 hash as well as a friendly version
 * that encodes the first 7-and-a-half bytes of that hash (roughly a GitHub
 * short hash).
 *
 */
export async function getFriendlyHash(input: string) {
  const encoder = new TextEncoder();
  const data = encoder.encode(input);
  const hash = await crypto.subtle.digest("SHA-256", data);

  const bytes = new Uint8Array(hash);
  const b1 = bytes[0];
  const b2 = bytes[1];
  const b3 = bytes[2];
  const b4 = bytes[3];

  return {
    bytes,
    hash: [...bytes].map((b) => b.toString(16)).join(""),
    friendly: [
      { value: b1, str: b1.toString(16), word: ADJ_256[b1] },
      { value: b2, str: b2.toString(16), word: ADJ_256[b2] },
      { value: b3, str: b3.toString(16), word: ADJ_256[b3] },
      { value: b4 >> 2, str: b4.toString(16), word: NOUNS_64[b2 >> 4] },
    ],
  };
}
